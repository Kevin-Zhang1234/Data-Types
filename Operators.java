public class Operators
{
    public static void main(String[] args) {
        int i = 10;
        double f = 2.5;
        boolean flag = true;
        f = f+i;
        flag = !flag;
        i += 1;
        System.out.println(i);
        System.out.println(f);
        System.out.println(flag);
    }
}
